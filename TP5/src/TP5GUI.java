    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String categoria = (String) jcbCategorias.getSelectedItem();
        System.out.println("Categoria Seleccionada: " + categoria);

        JTextField nombreTextField = new JTextField();
        JTextField precioTextField = new JTextField();

        Object[] message = {
            "nombre del producto:", nombreTextField,
            "Precio:", precioTextField
        };

        int option = JOptionPane.showConfirmDialog(null, message, "Agregar Productos",
                JOptionPane.OK_OPTION);

        if (option == JOptionPane.OK_OPTION) {
            //Si option es igual a 0 entonces entra a este if
            //Si option es igual a 1 entonces no entra al if
            String nombreProducto = nombreTextField.getText();
            String precioProducto = precioTextField.getText();
            System.out.println("nombre del producto: " + nombreProducto);
            System.out.println("precio del producto: " + precioProducto);

            Object[] datos ={nombreProducto,categoria,precioProducto};
            
            modelo.addRow(datos);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

